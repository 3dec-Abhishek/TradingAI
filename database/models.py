class Trade:


    def __init__(

        self,

        symbol,

        action,

        quantity,

        price,

        confidence,

        strategy,

        result,

        profit

    ):

        self.symbol = symbol

        self.action = action

        self.quantity = quantity

        self.price = price

        self.confidence = confidence

        self.strategy = strategy

        self.result = result

        self.profit = profit